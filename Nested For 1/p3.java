class ForDemo1{
       public static void main(String[] args){
                    int var=14;
                       for(int i=1;i<=4;i++){
                                    
                                   for(int j=1;j<=4;j++){
                                            System.out.print(var +" ");
                                                       
         }
                var++;
               System.out.println(" ");
           }
    }
}

