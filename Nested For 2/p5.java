class ForDemo4{
       public static void main(String[] args){
                       char ch='A';
                       char vr='a';
                       for(int i=1;i<=4;i++){
                                    
                                   for(int j=1;j<=4;j++){
                                       
                            if(j%2==0)   {           System.out.print(vr+" ");
                                            
                            }
                            else{
                                System.out.print(ch+" ");
                                
                            }
                            ch++;
                            vr++;
                        
                                                                  
         }
                
               System.out.println(" ");
           }
    }
}



